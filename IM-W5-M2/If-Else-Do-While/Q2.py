#2. take a input and check if a number is greater or less than given number.(let given number is 100)x=int(input())
x = input()
x = int(x)
if x > 100:
  print('The given number is greater than 100')
elif x < 100:
  print("The given number is less than 100")
elif x == 100:
  print("The given number is equal to 100")
else:
  print("Invalid input")
